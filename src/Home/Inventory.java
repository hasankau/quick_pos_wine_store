    private void selectItemStock() {
        try {
            int row = jTable1.getSelectedRow();
                        String ic = jTable1.getModel().getValueAt(row, 0).toString().trim();
                        item_code1.setText(ic);
            String barc = jTable1.getModel().getValueAt(row, 1).toString().trim();
            item_code.setText(barc);
            name.setText(jTable1.getModel().getValueAt(row, 2).toString().trim());
            desc.setText(jTable1.getModel().getValueAt(row, 3).toString().trim());
            manu.setSelectedItem(jTable1.getModel().getValueAt(row, 4).toString().trim());
            thresh.setText(jTable1.getModel().getValueAt(row, 7).toString().trim());
            Object q = jTable1.getModel().getValueAt(row, 8);

            i = db.getItem(barc);
            switch (i.getType().toLowerCase()) {
                case "item":
                    item.setSelected(true);
                    nonitem.setSelected(false);
                    break;
                case "nonitem":
                    nonitem.setSelected(true);
                    item.setSelected(false);
                    break;
            }

            if (q != null) {
                qty.setText(q.toString().trim());
            } else {
                qty.setText("0.0");
            }
            bp.setText(jTable1.getModel().getValueAt(row, 5).toString().trim());
            sp.setText(jTable1.getModel().getValueAt(row, 6).toString().trim());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
            e.printStackTrace();
        }
    }

