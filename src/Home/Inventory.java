    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String id = item_code.getText().trim();

        if (!jTable1.getSelectionModel().isSelectionEmpty() && id != null) {

            Toolkit.getDefaultToolkit().beep();
            int i = JOptionPane.showConfirmDialog(null, "Are you sure you want to delete item " + id + " and it's stocks?");
            if (i == JOptionPane.YES_OPTION) {
                home.setProcess("Deleting item");

                boolean b = db.deleteFromItem(id);

                if (b) {
                    tb.update_table("select id, barcode, name, description, manufacturer, purchase_price, selling_price, threshold_value, qty from item", jTable1);

                    home.setProcess("Success");
                } else {
                    home.setProcess("Error Occured");
                }

            }
        } else {
            Toolkit.getDefaultToolkit().beep();
            JOptionPane.showMessageDialog(null, "No row selected", "Error", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_jButton3ActionPerformed
