    private void jButton9ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton9ActionPerformed
        JFileChooser fc = new JFileChooser();
        FileNameExtensionFilter fnef = new FileNameExtensionFilter("*.All image Files", "jpg", "png", "gif", "bmp");
        fc.addChoosableFileFilter(fnef);
        int ResultDialog = fc.showOpenDialog(null);
        if (ResultDialog == JFileChooser.APPROVE_OPTION) {
            b = true;
            File selectedfile = fc.getSelectedFile();

            try {

                BufferedImage bi = ImageIO.read(selectedfile);
                Image image = bi.getScaledInstance(display.getWidth(), display.getHeight(), bi.SCALE_DEFAULT);

                ImageIcon ii = new ImageIcon(image);
                display.setIcon(ii);

                File f = new File(item_code.getText() + ".jpg");
                path = f.getAbsolutePath();
                ImageIO.write(bi, "JPEG", f);

            } catch (Exception e) {
                e.printStackTrace();
            }

        }
    }//GEN-LAST:event_jButton9ActionPerformed

